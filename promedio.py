nombre = str(input("Introduzca su nombre"))
notamat = float(input("Introduzca su nota matematica"))
notaleg = float(input("Introduzca su nota lenguaje"))
notaart = float(input("Introduzca su nota artes"))
promedio = float((notamat+notaleg+notaart)/3)

print ("El alumno", nombre, "tiene un promedio de:",  promedio)

if promedio >= 5 : 
    print ("Felicidades", nombre, "aprobaste el año")
else :
    print ("No te deprimas", nombre, "el año te irá mejor")
